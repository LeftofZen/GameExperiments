	public class Game1 : Game
	{
		GraphicsDeviceManager _graphics;
		SpriteBatch _spriteBatch;
		Dictionary<string, SpriteFont> _fonts = [];
		RasterizerState _rasterizerState = new RasterizerState() { ScissorTestEnable = true };

		float Scale = 0.5f;
		int ColumnWidth = 500;

		public Game1()
		{
			_graphics = new GraphicsDeviceManager(this);
			Content.RootDirectory = "Content";
			IsMouseVisible = true;
			Window.AllowUserResizing = true;
			Window.Title = "Font Demonstration";
		}

		protected override void Initialize()
		{
			// TODO: Add your initialization logic here

			base.Initialize();
		}

		protected override void LoadContent()
		{
			_spriteBatch = new SpriteBatch(GraphicsDevice);

			foreach (var fontName in typeof(FontNames).GetFields())
			{
				var fontValue = fontName.GetValue(null) as string;
				if (fontValue != null)
				{
					_fonts.Add(fontValue, Content.Load<SpriteFont>($"Fonts\\{fontValue}"));
				}
			}

			// TODO: use this.Content to load your game content here
		}

		protected override void Update(GameTime gameTime)
		{
			if (GamePad.GetState(PlayerIndex.One).Buttons.Back == ButtonState.Pressed || Keyboard.GetState().IsKeyDown(Keys.Escape))
			{
				Exit();
			}

			if (Keyboard.GetState().IsKeyDown(Keys.OemPlus) || Keyboard.GetState().IsKeyDown(Keys.Add))
			{
				Scale *= 1.005f;
			}
			else if (Keyboard.GetState().IsKeyDown(Keys.OemMinus) || Keyboard.GetState().IsKeyDown(Keys.Subtract))
			{
				Scale /= 1.005f;
			}

			if (Keyboard.GetState().IsKeyDown(Keys.Left))
			{
				ColumnWidth -= 5;
			}
			else if (Keyboard.GetState().IsKeyDown(Keys.Right))
			{
				ColumnWidth += 5;
			}

			Window.Title = $"Font Demonstration - Scale: {Scale:F2}";

			// TODO: Add your update logic here

			base.Update(gameTime);
		}

		protected override void Draw(GameTime gameTime)
		{
			GraphicsDevice.Clear(Color.Black);

			_spriteBatch.Begin(
				SpriteSortMode.Immediate,
				samplerState: SamplerState.PointClamp,
				rasterizerState: _rasterizerState);

			var width = Window.ClientBounds.Width;
			var height = Window.ClientBounds.Height;

			const int spacing = 8;
			var currX = spacing;
			var currY = spacing;
			var index = 0;

			foreach (var font in _fonts)
			{
				var fontSize = font.Value.MeasureString(font.Key) * Scale;

				if (currY + fontSize.Y + spacing > height)
				{
					currX += ColumnWidth + spacing;
					currY = spacing;
				}

				_spriteBatch.GraphicsDevice.ScissorRectangle = new Rectangle(currX, currY, ColumnWidth, (int)fontSize.Y);

				_spriteBatch.DrawString(
					font.Value,
					$"{font.Key} - ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz", //$"Font: {font.Key}",
					new Vector2(currX, currY),
					Color.White,
					0f,
					Vector2.Zero,
					Scale,
					SpriteEffects.None,
					0);


				currY += (int)fontSize.Y + spacing;
				index++;
			}

			_spriteBatch.End();

			base.Draw(gameTime);
		}
	}
